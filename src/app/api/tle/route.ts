export async function GET() {
  // Try fetching from Celestrak
  for (const url of CELESTRAK_URLS) {
    try {
      const response = await fetch(url, {
        signal: AbortSignal.timeout(6000),
        headers: {
          "User-Agent": "SENTINEL/0.1 satellite-tracker",
        },
        next: { revalidate: 3600 }, // Cache for 1 hour
      });

      if (response.ok) {
        const text = await response.text();
        // Validate it looks like TLE data
        if (text.includes("1 ") && text.includes("2 ") && text.length > 100) {
          return new NextResponse(text, {
            headers: { "Content-Type": "text/plain" },
          });
        }
      }
    } catch {
      continue;
    }
  }

  // Return fallback data
  return new NextResponse(FALLBACK_TLE, {
    headers: { "Content-Type": "text/plain" },
  });
}
